package StringDemos;

public class Demo8 {
	
	public static void main(String args[])
	{
		//Program for Searching Characters in Strings.
		
		String s2 = "Ahmedabad";
		
		System.out.println(s2.indexOf("m"));
		
		System.out.println("-------------");
		
		System.out.println(s2.lastIndexOf('h'));

		System.out.println("--------------");
		
		System.out.println(s2.charAt(5));
		
		System.out.println("--------------");
		
	}
}
