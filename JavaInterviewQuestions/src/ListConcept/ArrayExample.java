package ListConcept;

public class ArrayExample {

	public static void main(String args[]) {

		int array1[] = new int[5];
		array1[0] = 100; //0
		array1[1] = 200; //1
		array1[2] = 300; //2
		array1[3] = 400; //3
		array1[4] = 500; //4

		for (int i = 0; i < array1.length; i++) {
			System.out.println(array1[i]);
		}
	}

}
