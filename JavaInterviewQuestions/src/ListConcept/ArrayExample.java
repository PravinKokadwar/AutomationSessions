package ListConcept;

public class ArrayExample {

	public static void main(String args[]) {

		int array1[] = new int[5];
		array1[0] = 100;
		array1[1] = 200;
		array1[2] = 300;
		array1[3] = 400;
		array1[4] = 500;

		for (int i = 0; i < array1.length; i++) {
			System.out.println(array1[i]);
		}
	}

}
