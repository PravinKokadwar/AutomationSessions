package JavaPractice;

public class TwoTableProg {

	public static void main(String args[]) {

		int no = 2; // used for 2 table printing.
		// int no= 5; // used for 5 table printing.

		for (int i = 1; i <= 10; i++) {
			// System.out.println(no * i);
			System.out.println(no + "*" + i + "=" + no * i);

		}

	}

}
